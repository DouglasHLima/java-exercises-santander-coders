package com.douglas.input_output_exercises.math.calculator;

import java.util.Arrays;

public class Calculator {

    public static int sum(int firtsNumber, int secondNumber){
        return firtsNumber + secondNumber;
    }
    public static int subtract(int firtsNumber, int secondNumber){
        return firtsNumber - secondNumber;
    }
    public static int multiply(int firtsNumber, int secondNumber){
        return firtsNumber * secondNumber;
    }
    public static double divide(int firtsNumber, int secondNumber){
        return (double) firtsNumber / secondNumber;
    }


}
